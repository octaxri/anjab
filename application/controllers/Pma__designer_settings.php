<?php

if (!defined('BASEPATH'))
    exit('No direct script access allowed');

class Pma__designer_settings extends CI_Controller
{
    function __construct()
    {
        parent::__construct();

        $this->load->model('Pma__designer_settings_model');
        $this->load->library('form_validation');

        if(!$this->session->userdata('logined') || $this->session->userdata('logined') != true)
        {
            redirect('/');
        }        
	$this->load->library('datatables');
    }

    public function index()
    {
        $this->load->view('pma__designer_settings/pma__designer_settings_list');
    } 
    
    public function json() {
        header('Content-Type: application/json');
        echo $this->Pma__designer_settings_model->json();
    }

    public function read($id) 
    {
        $row = $this->Pma__designer_settings_model->get_by_id($id);
        if ($row) {
            $data = array(
		'username' => $row->username,
		'settings_data' => $row->settings_data,
	    );
            $this->load->view('pma__designer_settings/pma__designer_settings_read', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('pma__designer_settings'));
        }
    }

    public function create() 
    {
        $data = array(
            'button' => 'Create',
            'action' => site_url('pma__designer_settings/create_action'),
	    'username' => set_value('username'),
	    'settings_data' => set_value('settings_data'),
	);
        $this->load->view('pma__designer_settings/pma__designer_settings_form', $data);
    }
    
    public function create_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->create();
        } else {
            $data = array(
		'settings_data' => $this->input->post('settings_data',TRUE),
	    );

            $this->Pma__designer_settings_model->insert($data);
            $this->session->set_flashdata('message', 'Create Record Success');
            redirect(site_url('pma__designer_settings'));
        }
    }
    
    public function update($id) 
    {
        $row = $this->Pma__designer_settings_model->get_by_id($id);

        if ($row) {
            $data = array(
                'button' => 'Update',
                'action' => site_url('pma__designer_settings/update_action'),
		'username' => set_value('username', $row->username),
		'settings_data' => set_value('settings_data', $row->settings_data),
	    );
            $this->load->view('pma__designer_settings/pma__designer_settings_form', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('pma__designer_settings'));
        }
    }
    
    public function update_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->update($this->input->post('username', TRUE));
        } else {
            $data = array(
		'settings_data' => $this->input->post('settings_data',TRUE),
	    );

            $this->Pma__designer_settings_model->update($this->input->post('username', TRUE), $data);
            $this->session->set_flashdata('message', 'Update Record Success');
            redirect(site_url('pma__designer_settings'));
        }
    }
    
    public function delete($id) 
    {
        $row = $this->Pma__designer_settings_model->get_by_id($id);

        if ($row) {
            $this->Pma__designer_settings_model->delete($id);
            $this->session->set_flashdata('message', 'Delete Record Success');
            redirect(site_url('pma__designer_settings'));
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('pma__designer_settings'));
        }
    }

    public function _rules() 
    {
	$this->form_validation->set_rules('settings_data', 'settings data', 'trim|required');

	$this->form_validation->set_rules('username', 'username', 'trim');
	$this->form_validation->set_error_delimiters('<span class="text-danger">', '</span>');
    }

}

