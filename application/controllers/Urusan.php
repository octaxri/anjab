<?php

if (!defined('BASEPATH'))
    exit('No direct script access allowed');

class Urusan extends CI_Controller
{
    function __construct()
    {
        parent::__construct();

        $this->load->model('Urusan_model');
        $this->load->library('form_validation');

        if(!$this->session->userdata('logined') || $this->session->userdata('logined') != true)
        {
            redirect('/');
        }        
	$this->load->library('datatables');
    }

    public function index()
    {
        $this->load->view('urusan/urusan_list');
    } 
    
    public function json() {
        header('Content-Type: application/json');
        echo $this->Urusan_model->json();
    }

    public function read($id) 
    {
        $row = $this->Urusan_model->get_by_id($id);
        if ($row) {
            $data = array(
		'id' => $row->id,
		'nama_urusan' => $row->nama_urusan,
	    );
            $this->load->view('urusan/urusan_read', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('urusan'));
        }
    }

    public function create() 
    {
        $data = array(
            'button' => 'Create',
            'action' => site_url('urusan/create_action'),
	    'id' => set_value('id'),
	    'nama_urusan' => set_value('nama_urusan'),
	);
        $this->load->view('urusan/urusan_form', $data);
    }
    
    public function create_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->create();
        } else {
            $data = array(
		'nama_urusan' => $this->input->post('nama_urusan',TRUE),
	    );

            $this->Urusan_model->insert($data);
            $this->session->set_flashdata('message', 'Create Record Success');
            redirect(site_url('urusan'));
        }
    }
    
    public function update($id) 
    {
        $row = $this->Urusan_model->get_by_id($id);

        if ($row) {
            $data = array(
                'button' => 'Update',
                'action' => site_url('urusan/update_action'),
		'id' => set_value('id', $row->id),
		'nama_urusan' => set_value('nama_urusan', $row->nama_urusan),
	    );
            $this->load->view('urusan/urusan_form', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('urusan'));
        }
    }
    
    public function update_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->update($this->input->post('id', TRUE));
        } else {
            $data = array(
		'nama_urusan' => $this->input->post('nama_urusan',TRUE),
	    );

            $this->Urusan_model->update($this->input->post('id', TRUE), $data);
            $this->session->set_flashdata('message', 'Update Record Success');
            redirect(site_url('urusan'));
        }
    }
    
    public function delete($id) 
    {
        $row = $this->Urusan_model->get_by_id($id);

        if ($row) {
            $this->Urusan_model->delete($id);
            $this->session->set_flashdata('message', 'Delete Record Success');
            redirect(site_url('urusan'));
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('urusan'));
        }
    }

    public function _rules() 
    {
	$this->form_validation->set_rules('nama_urusan', 'nama urusan', 'trim|required');

	$this->form_validation->set_rules('id', 'id', 'trim');
	$this->form_validation->set_error_delimiters('<span class="text-danger">', '</span>');
    }

}

