<?php

if (!defined('BASEPATH'))
    exit('No direct script access allowed');

class Rumpun_jabatan extends CI_Controller
{
    function __construct()
    {
        parent::__construct();

        $this->load->model('Rumpun_jabatan_model');
        $this->load->library('form_validation');

        if(!$this->session->userdata('logined') || $this->session->userdata('logined') != true)
        {
            redirect('/');
        }        
	$this->load->library('datatables');
    }

    public function index()
    {
        $this->load->view('rumpun_jabatan/rumpun_jabatan_list');
    } 
    
    public function json() {
        header('Content-Type: application/json');
        echo $this->Rumpun_jabatan_model->json();
    }

    public function read($id) 
    {
        $row = $this->Rumpun_jabatan_model->get_by_id($id);
        if ($row) {
            $data = array(
		'id' => $row->id,
		'nama_rumpun' => $row->nama_rumpun,
	    );
            $this->load->view('rumpun_jabatan/rumpun_jabatan_read', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('rumpun_jabatan'));
        }
    }

    public function create() 
    {
        $data = array(
            'button' => 'Create',
            'action' => site_url('rumpun_jabatan/create_action'),
	    'id' => set_value('id'),
	    'nama_rumpun' => set_value('nama_rumpun'),
	);
        $this->load->view('rumpun_jabatan/rumpun_jabatan_form', $data);
    }
    
    public function create_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->create();
        } else {
            $data = array(
		'nama_rumpun' => $this->input->post('nama_rumpun',TRUE),
	    );

            $this->Rumpun_jabatan_model->insert($data);
            $this->session->set_flashdata('message', 'Create Record Success');
            redirect(site_url('rumpun_jabatan'));
        }
    }
    
    public function update($id) 
    {
        $row = $this->Rumpun_jabatan_model->get_by_id($id);

        if ($row) {
            $data = array(
                'button' => 'Update',
                'action' => site_url('rumpun_jabatan/update_action'),
		'id' => set_value('id', $row->id),
		'nama_rumpun' => set_value('nama_rumpun', $row->nama_rumpun),
	    );
            $this->load->view('rumpun_jabatan/rumpun_jabatan_form', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('rumpun_jabatan'));
        }
    }
    
    public function update_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->update($this->input->post('id', TRUE));
        } else {
            $data = array(
		'nama_rumpun' => $this->input->post('nama_rumpun',TRUE),
	    );

            $this->Rumpun_jabatan_model->update($this->input->post('id', TRUE), $data);
            $this->session->set_flashdata('message', 'Update Record Success');
            redirect(site_url('rumpun_jabatan'));
        }
    }
    
    public function delete($id) 
    {
        $row = $this->Rumpun_jabatan_model->get_by_id($id);

        if ($row) {
            $this->Rumpun_jabatan_model->delete($id);
            $this->session->set_flashdata('message', 'Delete Record Success');
            redirect(site_url('rumpun_jabatan'));
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('rumpun_jabatan'));
        }
    }

    public function _rules() 
    {
	$this->form_validation->set_rules('nama_rumpun', 'nama rumpun', 'trim|required');

	$this->form_validation->set_rules('id', 'id', 'trim');
	$this->form_validation->set_error_delimiters('<span class="text-danger">', '</span>');
    }

}

