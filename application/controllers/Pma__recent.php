<?php

if (!defined('BASEPATH'))
    exit('No direct script access allowed');

class Pma__recent extends CI_Controller
{
    function __construct()
    {
        parent::__construct();

        $this->load->model('Pma__recent_model');
        $this->load->library('form_validation');

        if(!$this->session->userdata('logined') || $this->session->userdata('logined') != true)
        {
            redirect('/');
        }        
	$this->load->library('datatables');
    }

    public function index()
    {
        $this->load->view('pma__recent/pma__recent_list');
    } 
    
    public function json() {
        header('Content-Type: application/json');
        echo $this->Pma__recent_model->json();
    }

    public function read($id) 
    {
        $row = $this->Pma__recent_model->get_by_id($id);
        if ($row) {
            $data = array(
		'username' => $row->username,
		'tables' => $row->tables,
	    );
            $this->load->view('pma__recent/pma__recent_read', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('pma__recent'));
        }
    }

    public function create() 
    {
        $data = array(
            'button' => 'Create',
            'action' => site_url('pma__recent/create_action'),
	    'username' => set_value('username'),
	    'tables' => set_value('tables'),
	);
        $this->load->view('pma__recent/pma__recent_form', $data);
    }
    
    public function create_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->create();
        } else {
            $data = array(
		'tables' => $this->input->post('tables',TRUE),
	    );

            $this->Pma__recent_model->insert($data);
            $this->session->set_flashdata('message', 'Create Record Success');
            redirect(site_url('pma__recent'));
        }
    }
    
    public function update($id) 
    {
        $row = $this->Pma__recent_model->get_by_id($id);

        if ($row) {
            $data = array(
                'button' => 'Update',
                'action' => site_url('pma__recent/update_action'),
		'username' => set_value('username', $row->username),
		'tables' => set_value('tables', $row->tables),
	    );
            $this->load->view('pma__recent/pma__recent_form', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('pma__recent'));
        }
    }
    
    public function update_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->update($this->input->post('username', TRUE));
        } else {
            $data = array(
		'tables' => $this->input->post('tables',TRUE),
	    );

            $this->Pma__recent_model->update($this->input->post('username', TRUE), $data);
            $this->session->set_flashdata('message', 'Update Record Success');
            redirect(site_url('pma__recent'));
        }
    }
    
    public function delete($id) 
    {
        $row = $this->Pma__recent_model->get_by_id($id);

        if ($row) {
            $this->Pma__recent_model->delete($id);
            $this->session->set_flashdata('message', 'Delete Record Success');
            redirect(site_url('pma__recent'));
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('pma__recent'));
        }
    }

    public function _rules() 
    {
	$this->form_validation->set_rules('tables', 'tables', 'trim|required');

	$this->form_validation->set_rules('username', 'username', 'trim');
	$this->form_validation->set_error_delimiters('<span class="text-danger">', '</span>');
    }

}

